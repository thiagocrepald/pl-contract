CREATE TABLE `MEDICO_CONTRATO_ANEXO` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `MEDICO_ID` int(11) NOT NULL,
  `CONTRATO_ANEXO_ID` int(11) NOT NULL,
