  KEY `FK_MCA_MEDICO_ID` (`MEDICO_ID`),
  KEY `FK_MCA_CONTRATO_ANEXO_ID` (`CONTRATO_ANEXO_ID`),
  CONSTRAINT `FK_MCA_CONTRATO_ANEXO_ID` FOREIGN KEY (`CONTRATO_ANEXO_ID`) REFERENCES `CONTRATO_ANEXO` (`ID`),
